// í™˜ê²½ ë³€ìˆ˜ í™•ì¸ í•¨ìˆ˜
function getEnvironmentVariables() {
  const supabaseUrl = process.env.VITE_SUPABASE_URL || process.env.SUPABASE_URL
  const supabaseAnonKey = process.env.VITE_SUPABASE_ANON_KEY || process.env.SUPABASE_ANON_KEY
  
  return { supabaseUrl, supabaseAnonKey }
}

// Supabase í´ë¼ì´ì–¸íŠ¸ ìƒì„± í•¨ìˆ˜
function createSupabaseClient() {
  const { supabaseUrl, supabaseAnonKey } = getEnvironmentVariables()
  
  if (!supabaseUrl || !supabaseAnonKey) {
    throw new Error('Supabase configuration missing')
  }
  
  try {
    return createClient(supabaseUrl, supabaseAnonKey)
  } catch (error) {
    console.error('Failed to create Supabase client:', error)
    throw error
  }
}

// POST /api/auth - ì‚¬ìš©ì ë¡œê·¸ì¸ (03_ì‚¬ìš©ì_ë¡œê·¸ì¸.xlsx í˜•ì‹ì— ë§ì¶¤)
export default async (req, res) => {
  // IP ì ‘ê·¼ ê¶Œí•œ í™•ì¸
  const ipCheck = checkIPAccess(req)
  if (!ipCheck.allowed) {
    return res.status(403).json(ipCheck.error)
  }

  // POST ë©”ì„œë“œë§Œ í—ˆìš©
  if (req.method !== 'POST') {
    return res.status(405).json({
      success: false,
      message: 'Method not allowed'
    })
  }
  try {
    const { email, password } = req.body
    
    // í•„ìˆ˜ íŒŒë¼ë¯¸í„° ê²€ì¦
    if (!email || !password) {
      return res.status(400).json({
        success: false,
        message: 'ì´ë©”ì¼ê³¼ ë¹„ë°€ë²ˆí˜¸ë¥¼ ì…ë ¥í•´ì£¼ì„¸ìš”.'
      })
    }

    // Supabase í´ë¼ì´ì–¸íŠ¸ ìƒì„±
    const supabase = createSupabaseClient()

    // ì‚¬ìš©ì ì¸ì¦
    const { data, error } = await supabase.auth.signInWithPassword({
      email: email,
      password: password
    })

    if (error) {
      console.error('Authentication error:', error)
      return res.status(401).json({
        success: false,
        message: 'ì´ë©”ì¼ ë˜ëŠ” ë¹„ë°€ë²ˆí˜¸ê°€ ì˜¬ë°”ë¥´ì§€ ì•ŠìŠµë‹ˆë‹¤.',
        error: error.message
      })
    }

    if (!data.user || !data.session) {
      return res.status(401).json({
        success: false,
        message: 'ì¸ì¦ì— ì‹¤íŒ¨í–ˆìŠµë‹ˆë‹¤.'
      })
    }

    // ì‚¬ìš©ì ë©”íƒ€ë°ì´í„° ì¡°íšŒ (ì„ íƒì‚¬í•­)
    let userData = null
    try {
      const { data: profileData, error: profileError } = await supabase
        .from('profiles')
        .select('*')
        .eq('id', data.user.id)
        .single()

      if (!profileError && profileData) {
        userData = profileData
      }
    } catch (profileError) {
      console.log('Profile fetch error (non-critical):', profileError)
    }

    // 03_ì‚¬ìš©ì_ë¡œê·¸ì¸.xlsx í˜•ì‹ì— ë§ì¶˜ ì‘ë‹µ
    const response = {
      success: true,
      message: 'ì¸ì¦ ì„±ê³µ',
      data: {
        token: data.session.access_token,
        refreshToken: data.session.refresh_token,
        user: userData || {
          id: data.user.id,
          email: data.user.email,
          created_at: data.user.created_at,
          last_sign_in_at: data.user.last_sign_in_at,
          user_metadata: data.user.user_metadata || {}
        },
        expiresIn: '24h',
        expiresAt: new Date(data.session.expires_at * 1000).toISOString()
      }
    }

    res.json(response)

  } catch (error) {
    console.error('Auth API error:', error)
    res.status(500).json({
      success: false,
      message: 'ì„œë²„ ì˜¤ë¥˜ê°€ ë°œìƒí–ˆìŠµë‹ˆë‹¤.',
      error: error.message
    })
  }
}